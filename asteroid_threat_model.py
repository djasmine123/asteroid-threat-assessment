def calculate_diameter_HG(H, albedo):
    """
    Calculate asteroid diameter from absolute magnitude using H-G system

    Formula: D = 1329 / sqrt(p_v) * 10^(-H/5)

    Where:
        D = diameter (km)
        H = absolute magnitude
        p_v = geometric albedo

    Reference:
        Fowler & Chillemi (1992), "The IRAS Minor Planet Survey"

    Uncertainty:
        ±30% typical when albedo is assumed/inferred
        ±10% when albedo is measured

    Parameters:
        H (float): Absolute magnitude
        albedo (float): Geometric albedo

    Returns:
        float: Diameter in kilometers
    """
    try:
        H = float(H)
        albedo = float(albedo)

        # Validate inputs
        if albedo <= 0 or albedo > 1:
            albedo = 0.15  # Fallback to typical value

        if pd.isna(H):
            return np.nan

        # H-G formula (standard in asteroid research)
        diameter = 1329.0 / np.sqrt(albedo) * np.power(10.0, -H/5.0)

        return diameter
    except:
        return np.nan

