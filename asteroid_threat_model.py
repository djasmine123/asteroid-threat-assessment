    # OPTIMIZATION 3: More patience for longer training
    best_fold_val = 0
    patience_counter = 0
    patience = 20  # Extended patience

    for epoch in range(100):
        # OPTIMIZATION 4: Training with REDUCED data augmentation noise
        fold_model.train()
        for batch in fold_train_loader:
            optical = batch['optical'].to(device)
            radar = batch['radar'].to(device)
            spectral_type = batch['spectral_type'].to(device)
            spectral_continuous = batch['spectral_continuous'].to(device)
            labels = batch['label'].squeeze(1).to(device)

            # REDUCED noise (0.02 instead of 0.03)
            if np.random.random() < 0.5:
                optical = optical + torch.randn_like(optical) * 0.02  # ← CHANGED
                radar = radar + torch.randn_like(radar) * 0.02  # ← CHANGED
                spectral_continuous = spectral_continuous + torch.randn_like(spectral_continuous) * 0.02  # ← CHANGED

            optimizer.zero_grad()
            outputs = fold_model(optical, radar, spectral_type, spectral_continuous)
            loss = criterion(outputs, labels)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(fold_model.parameters(), max_norm=1.0)
            optimizer.step()

        # Validate and update LR
        fold_model.eval()
        val_correct = 0
        val_total = 0
        with torch.no_grad():
            for batch in fold_val_loader:
                optical = batch['optical'].to(device)
                radar = batch['radar'].to(device)
                spectral_type = batch['spectral_type'].to(device)
                spectral_continuous = batch['spectral_continuous'].to(device)
                labels = batch['label'].squeeze(1).to(device)

                outputs = fold_model(optical, radar, spectral_type, spectral_continuous)
                _, predicted = outputs.max(1)
                val_total += labels.size(0)
                val_correct += predicted.eq(labels).sum().item()

        val_acc = 100. * val_correct / val_total
        scheduler.step(val_acc)

        # Save best model
        if val_acc > best_fold_val:
            best_fold_val = val_acc
            patience_counter = 0
            torch.save(fold_model.state_dict(), f'fold_{fold}_best.pth')
            if epoch > 0 and epoch % 10 == 0:  # Log progress every 10 epochs
                print(f"    Epoch {epoch}: New best {val_acc:.2f}%")
        else:
            patience_counter += 1
            if patience_counter >= patience:
                print(f"    Early stopping at epoch {epoch+1} (Best: {best_fold_val:.2f}%)")
                break

    # Load best model (CRITICAL!)
    fold_model.load_state_dict(torch.load(f'fold_{fold}_best.pth'))

    # Evaluate this fold
    fold_model.eval()
    correct = 0
    total = 0
    all_preds = []
    all_true = []

    with torch.no_grad():
        for batch in fold_val_loader:
            optical = batch['optical'].to(device)
            radar = batch['radar'].to(device)
            spectral_type = batch['spectral_type'].to(device)
            spectral_continuous = batch['spectral_continuous'].to(device)
            labels = batch['label'].squeeze(1).to(device)

            outputs = fold_model(optical, radar, spectral_type, spectral_continuous)
            _, predicted = outputs.max(1)

            total += labels.size(0)
            correct += predicted.eq(labels).sum().item()

            all_preds.extend(predicted.cpu().numpy())
            all_true.extend(labels.cpu().numpy())

    fold_acc = 100. * correct / total
    fold_f1 = f1_score(all_true, all_preds, average='weighted')

    fold_results.append({
        'fold': fold,
        'accuracy': fold_acc,
        'f1': fold_f1
    })

    print(f"  ✅ Final Accuracy: {fold_acc:.2f}%")
    print(f"  ✅ F1-Score: {fold_f1:.4f}")

